#python program to demonstrate bitwise operators

a = 60            # 60 = 0011 1100
b = 13            # 13 = 0000 1101

# bitwise AND 
print("a & b =", a & b)

# bitwise OR
print("a | b =", a | b)

# bitwise NOT
print("~a =", ~a)

#bitwise XOR
print("a ^ b =", a ^ b)

#bitwise left shift
print("a << 2 =", a << 2)

#bitwise right shift
print("a >> 2 =", a >> 2)

