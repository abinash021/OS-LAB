# write a python program to display various assignment operators

a = 10
b = 20

# assignment operator
c = a+b
print("a+b = ", c)

# add and assign
c += a
print("c += a = ", c)

# subtract and assign
c -= a
print("c -= a = ", c)

# multiply and assign
c *= a
print("c *= a = ", c)

# divide and assign
c /= a
print("c /= a = ", c)

# divide floor and assign
c //= a
print("c //= a = ", c)

# Exponent and assign
c **= a
print("c **= a = ", c)
